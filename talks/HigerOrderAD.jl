# ╔═╡ dc56466c-3a65-45db-9a99-e1a86a693afa
function fourth_derivative_fd(f::F, u, du, ddu, dddu, ddddu) where F
    df = (
          f(u + 2.0 * du + 2.0 * ddu + 4.0/3.0 * dddu + 2.0/3.0 * ddddu)
         - 4.0 * f(u + du + 0.5 * ddu + 1.0/6.0 * dddu + 1.0/24.0 * ddddu)
         + 6.0 * f(u)
         - 4.0 * f(u - du + 0.5 * ddu - 1.0/6.0 * dddu + 1.0/24.0 * ddddu)
         + f(u - 2.0 * du + 2.0 * ddu - 4.0/3.0 * dddu + 2.0/3.0 * ddddu)
         )
    return df
end

