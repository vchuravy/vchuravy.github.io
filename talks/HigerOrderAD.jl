# ╔═╡ f702b0cb-a880-47b8-bf10-096db3db9c57
function third_derivative_fd(f::F, u, du, ddu, dddu) where F
    factor = 0.5
    df = factor * (f(u + 2.0 * du + 2.0 * ddu + 4.0/3.0 * dddu)
                   - 2.0 * f(u + du + 0.5 * ddu + (1.0/6.0) * dddu)
                   + 2.0 * f(u - du + 0.5 * ddu - (1.0/6.0) * dddu)
                   - f(u - 2.0 * du + 2.0 * ddu - 4.0/3.0 * dddu))
    return df
end

# ╔═╡ 050dc757-278e-4d8c-a10d-debdd656a987
@inline @inbounds function dddf_taylor_diff(f::F, u, du, ddu, dddu) where F
