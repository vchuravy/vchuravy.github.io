# ╔═╡ 2ce58128-220e-4947-b5cf-0cf8c02b0062
md"""
One way of thinking about forward mode automatic differentiation is through the lens of dual numbers.


Consider a taylor series for `f(x)`. `f'(x)` is the epsilon coefficient. 

```math
f(x+\epsilon) = f(x) + f'(x) \epsilon + f''(x) \epsilon^2 + ...
```

At small epsilon, we find the derivative on the epsilon term

```math
f(x+\epsilon) = f(x) + f'(x) \epsilon, \epsilon << 1
```

Forward-mode AD can be implemented nicely through the use of dual numbers. For each variable of our original program, we simply store both the original number and the epsilon term.

```math
z = x + y * \epsilon, \epsilon << 1
```

We can also use this trick to determine the corresponding derivative rule for any instruction.


Let's try add. Let's take two dual numbers and add them up.

```math
(a + b \epsilon) + (c + d \epsilon) = (a + c) + (b + d)\epsilon
```

What about mulitiply?


```math
(a + b \epsilon) * (c + d \epsilon) = ac + (ad + bc) \epsilon + bd \epsilon^2
```

Here we find three terms going all the way up to epsilon^2. However, since we know epsilon << 1, we can cut it off and find the final result of
```math
ac + (ad + bc) \epsilon
```.

Enzyme implements these rules or all the primitive LLVM instructions. By implementing the derivative rule for all these instructions, one can apply the chain rule between them to find the total derivative of a computation. Let's see how it works in practice.
"""

# ╔═╡ f53361e8-d254-40c6-941a-c85e8f2e213b
with_terminal() do
	Enzyme.Compiler.enzyme_code_llvm(
		simple_mlp,
		Duplicated,
		Tuple{
			Duplicated{Float64},
			Duplicated{Float64},
			Duplicated{Float64}},
		mode=Enzyme.API.DEM_ForwardMode,
		debuginfo=:none
	)
end

# ╔═╡ 4c9fdae9-e452-447f-90ca-556c7f6cccbf
md"""
Julia adds a bit of extra garbage collection overhead, but we see the corresponding derivatives of the multiply and add instructions!

```
  %10 = fmul fast double %2, %1
  %11 = fmul fast double %3, %0
  %12 = fadd fast double %11, %10
  %13 = fadd fast double %12, %5
  %14 = insertvalue [1 x double] zeroinitializer, double %13, 0
  ret [1 x double] %14
```

However, what were those `Duplicated` objects, and what about the extra arguments?

Since we need to pass in both the original variable, and now an extra dual variable, Enzyme provides a convenient struct Duplicated which holds both. Here we set the input dual numbers all to 0, except for the dual of weight which is set to 1.0. In Enzyme terminology we like to refer to these extra variables as the corresponding shadow variables.

Thus as a result, we compute the derivative of the loss with respect to the weight!

Generalizing a bit, suppose we have a functions with many inputs and many outputs. Forward mode AD allows you to compute the directional derivative of each output in a single call. In other words it combines a Jacobian vector product.

Let's look at a concrete example. For our mlp function above suppose we set `dinput` as the shadow of `input`, `dweight` as the shadow of `weight` and `dbias` as the shadow of `bias`.

With our single call to forward mode we can compute the entire expression.

```math
\frac{\partial \text{loss}}{\partial \text{input}} dinput +
\frac{\partial \text{loss}}{\partial \text{weight}} dweight +
\frac{\partial \text{loss}}{\partial \text{bias}} dbias
```

This is sometimes referred to as a Jacobian-vector product in the literature or other tools.
"""


# ╔═╡ f26c03ab-5adb-45ee-834e-a3dcfa27d1a6
md"""
One thing someone can immediately notice from Enzyme's generated instructions, however, is that we have to do a lot of extra dual number arithmetic for the extra instructions that we pass in zeros to.

Fortunately, Enzyme has a nice way of solving this. Instead of marking a variable as `Duplicated`, you can instead mark it as `Const` meaning that it won't be differentiated with respect to.

Let's try it out on our dual number example.
"""


# ╔═╡ 2024d3ca-e636-4d47-8ca6-63a2a0ed2c66
with_terminal() do
