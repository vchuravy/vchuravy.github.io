		simple_mlp,
		Duplicated,
		Tuple{
			Const{Float64},
			Duplicated{Float64},
			Const{Float64}},
		mode=Enzyme.API.DEM_ForwardMode,
		debuginfo=:none
	)
end

# ╔═╡ 110b9b5e-6aef-404c-a5cc-4fc95592762f
md"""

```
  %8 = fmul fast double %2, %0
  %9 = insertvalue [1 x double] zeroinitializer, double %8, 0
  ret [1 x double] %9
```

Now we only have one extra floating point instruction, instead of three extra instructions. The code here is quite small so it may not matter much here, but on larger programs this can make a significant difference. Regardless, let's see how it runs
"""

