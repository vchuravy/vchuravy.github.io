md"""
## Jacobian Operator

Given a problem `F(u) = 0` we say that `F(u)` calculates the *residual*.
We want to have an operator `J(F, u)` that represents the Jacobian of `F` at `u`.
"""

# ╔═╡ 12798504-eb1a-4790-9984-08b669859b8b
function maybe_duplicated(f, df)
    if !Enzyme.Compiler.guaranteed_const(typeof(f))
        return DuplicatedNoNeed(f, df)
    else
        return Const(f)
    end
end

# ╔═╡ ff038270-a3e8-47c1-81ea-77f6474beb8a
begin
	struct JacobianOperator{F, A}
	    f::F # res = F(u)
	    u::A
		res::A
	end
	JacobianOperator(F, u) = JacobianOperator(F, u, F(u))
end

# ╔═╡ b6fd4c75-c503-41bd-b0ac-f0bc158f47b9
begin
	Base.size(J::JacobianOperator) = (length(J.res), length(J.u))
	Base.eltype(J::JacobianOperator) = eltype(J.u)
end

# ╔═╡ d266d90e-c43b-43d0-8630-30c1c18a0a75
function LinearAlgebra.mul!(out, J::JacobianOperator, v)
    f_cache = Enzyme.make_zero(J.f)
    dres = autodiff(
        Forward,
        maybe_duplicated(J.f, f_cache), Duplicated,
        DuplicatedNoNeed(J.u, reshape(v, size(J.u)))
    ) |> only
	out .= dres
    return nothing
end

# ╔═╡ 8b2759d2-0cf1-478c-afd7-0db6cf615b1b
function Base.collect(JOp::JacobianOperator)
    N, M = size(JOp)
    v = zeros(eltype(JOp), M)
    out = zeros(eltype(JOp), N)
    J = SparseMatrixCSC{eltype(v), Int}(undef, size(JOp)...)
    for j in 1:M
        out .= 0.0
        v .= 0.0
        v[j] = 1.0
        mul!(out, JOp, v)
        for i in 1:N
            if out[i] != 0
                J[i, j] = out[i]
            end
        end
    end
    return J
end

