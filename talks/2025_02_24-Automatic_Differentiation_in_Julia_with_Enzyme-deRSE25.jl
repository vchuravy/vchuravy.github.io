md"""
## Why do we want gradients/derivatives

Derivatives compute the rate of change of a function’s output with respect to input(s)

```math
f'(x) = \lim_{h\rightarrow 0} \frac{f(a+h)-f(a)}{h}
```

### Used in many places

- Optimization problems
- Parameter estimation
- Uncertainity quantification
- Machine learning
"""

# ╔═╡ 79407206-b24c-4add-ad1c-77f12cc8b6a4
md"""
## Methods of obtaining derivatives

- Manual coding
  - Tedious & error prone
- Numerical differentiation / Finite differences
  - Prone to numerical errors
  - Costly
- Symbolic differentiation
  - May only handle a subset of programs
  - Suffers from complexity explosion
- Automatic differentiation / Algebraic differentiation
  - Efficient (longer conversation)
  - Work-horse of Machine Learning
"""

# ╔═╡ 7a7826ff-82f8-4e6f-9f3b-9ee2cd13b51e
md"""
## Variants of automatic differentiation

- Differentiable DSL / Operator overloading
  - Provide a new language (embedded) designed to be differentiated
  - DSL must suppot all operations in orginal code
  - May require rewriting
- Source-rewriting
  - Statically analyze programs in the source language
  - Re-implementing parsing and semantics of a given language
  - Requires all code to be available ahead of time
  - Examples: Tapenade
- Compiler-enabled
  - Operates on common compiler intermediate representation
"""

